    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($page)
    {
        $count = PageVisit::where('page',$page)->count();
        if($count > 0){
          $pageVisited = PageVisit::where('page',$page)->first();
          $pageVisited->visits++;
          $pageVisited->save();
        }else {
            $pageVisit = new PageVisit();
            $pageVisit->page = $page;
            $pageVisit->visits = 1;
            $pageVisit->save();
        }
    }

